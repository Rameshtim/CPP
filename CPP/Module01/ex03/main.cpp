#include "HumanA.hpp"
#include "Weapon.hpp"
#include "HumanB.hpp"

int		main(void) {
	{
		Weapon club = Weapon("crude spiked club");

		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		HumanB jim("Jim");
		jim.attack();
		Weapon club;// = Weapon("crude spiked club");
		club.setType("some other type of club");
		jim.setWeapon(club);
		jim.attack();
	}

	return (0);
}